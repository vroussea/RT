#include <rt.h>

void	vect_prod(double u[3], double v[3], double orth[3])
{
	orth[0] = u[1] * v[2] - u[2] * v[1];
	orth[1] = u[2] * v[0] - u[0] * v[2];
	orth[2] = u[0] * v[1] - u[1] * v[0];
}